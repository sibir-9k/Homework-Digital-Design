<template>
	<svg>
		<use :xlink:href="nameIcon" />
	</svg>
</template>

<script>
export default {
	name: 'Icon',
	props: {
		iconName: {
			type: String,
		},
	},
	computed: {
		nameIcon() {
			return `#${this.iconName}`;
		},
	},
};
</script>

